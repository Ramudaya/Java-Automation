package LoopJumpingstatements;

public class preincrements {
	public static void main(String[] args) {
		int i = 1000;
		
		System.out.println(i++);
		System.out.println(i++);
		System.out.println(++i);
		System.out.println(++i);
		System.out.println(i++);
		System.out.println(i++);
		System.out.println(++i);
		System.out.println(++i);
		System.out.println(i++);
		System.out.println(++i);
		System.out.println(i++);
		System.out.println(i++);
		System.out.println(++i);
		System.out.println(++i);
		System.out.println(++i + i++);

	}
	

}
